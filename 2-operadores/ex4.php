<?php 

// operadores de comparação 

$a = 30; 
$b = 55; 

var_dump($a > $b); 
echo "<br>"; 

var_dump($a < $b); 
echo "<br>"; 

var_dump($a = $b); // um sinal de igual atribui valor 
echo "<br>"; 

var_dump($a == $b); // compara valor e não o tipo de variaveis 
echo "<br>"; 

var_dump($a === $b); // compara valores e tipos de variaveis 
echo "<br>"; 

var_dump($a != $b); // valores diferente 
echo "<br>"; 

var_dump($a !== $b); // valores e tipos diferentes 
echo "<br>"; 

?>